export function getSovereignAgent(id: string): SovereignAgentProfile | undefined {
  return sovereignAgents.find((agent) => agent.id === id);
}

export function listSovereignAgentsByDepartment(department: string): SovereignAgentProfile[] {
  return sovereignAgents.filter((agent) => agent.department === department);
}

export function recommendSovereignAgentsForMission(type: string): SovereignAgentProfile[] {
  const exact = sovereignAgents.filter((agent) => agent.canOwn.includes(type as any));
  if (exact.length >= 3) return exact.slice(0, 3);
  const fallback = sovereignAgents.filter((agent) => ['professor-apex', 'cmo-prime', 'pipeline-titan'].includes(agent.id));
  return Array.from(new Map([...exact, ...fallback].map((agent) => [agent.id, agent])).values()).slice(0, 3);
}
