export function getHumanStaffAgent(id: HumanStaffAgentId): HumanStaffAgent {
  const found = HUMAN_STAFF_AGENTS.find((agent) => agent.id === id);
  if (!found) return HUMAN_STAFF_AGENTS[0];
  return found;
}

export function getHumanStaffDepartment(id: HumanStaffDepartmentId): HumanStaffDepartment {
  const found = HUMAN_STAFF_DEPARTMENTS.find((department) => department.id === id);
  if (!found) return HUMAN_STAFF_DEPARTMENTS[0];
  return found;
}

export function getAgentsByDepartment(departmentId: HumanStaffDepartmentId): HumanStaffAgent[] {
  return HUMAN_STAFF_AGENTS.filter((agent) => agent.departmentId === departmentId);
}

export function recommendAgentsForMission(missionType: string): HumanStaffAgentId[] {
  const key = missionType.toLowerCase();
  if (key.includes('swarm') || key.includes('intel')) return ['pipeline_titan', 'scout_supreme', 'night_owl_intel'];
  if (key.includes('geo') || key.includes('city')) return ['geo_commander', 'local_news_radar', 'analytics_beast'];
  if (key.includes('appointment') || key.includes('booking')) return ['appointment_architect', 'liora_lifecycle', 'revenue_captain'];
  if (key.includes('sales') || key.includes('close')) return ['revenue_captain', 'appointment_architect', 'velvet_hammer'];
  if (key.includes('recruit')) return ['partner_recruiter', 'affiliate_wrangler', 'liora_lifecycle'];
  if (key.includes('content') || key.includes('creative')) return ['cmo_prime', 'goldframe', 'shorts_factory'];
  if (key.includes('pr') || key.includes('authority')) return ['pr_sentinel', 'cmo_prime', 'goldframe'];
  if (key.includes('automation') || key.includes('worker') || key.includes('queue')) return ['switchboard', 'pipeline_titan', 'analytics_beast'];
  if (key.includes('compliance')) return ['velvet_hammer', 'liora_lifecycle', 'professor_apex'];
  return ['professor_apex', 'cmo_prime', 'pipeline_titan'];
}
